// Fetch book data
$booksQuery = "
    SELECT b.BookId, b.Title, b.Availability,
    (SELECT MAX(Date_of_Issue) FROM record r WHERE r.BookId = b.BookId) AS LastIssued
    FROM book b
";
$booksResult = $conn->query($booksQuery);

$popular = [];
$lessFavored = [];
$currentDate = new DateTime();
$thresholdDays = 30;

while ($row = $booksResult->fetch_assoc()) {
    $title = $row['Title'];
    $availability = $row['Availability'];
    $lastIssued = $row['LastIssued'];

    if ((int)$availability === 0) {
        $popular[] = $title;
    } elseif ($lastIssued) {
        $issuedDate = new DateTime($lastIssued);
        $interval = $currentDate->diff($issuedDate)->days;
        if ($interval > $thresholdDays) {
            $lessFavored[] = $title;
        }
    }
}
